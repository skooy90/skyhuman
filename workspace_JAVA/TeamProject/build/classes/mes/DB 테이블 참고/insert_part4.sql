SELECT '총 입력된 데이터 수:' as INFO, 
       (SELECT COUNT(*) FROM USERS) as 사용자수,
       (SELECT COUNT(*) FROM STANDARD) as 제품수,
       (SELECT COUNT(*) FROM PRODUCTION) as 생산건수,
       (SELECT COUNT(*) FROM WORK) as 작업건수,
       (SELECT COUNT(*) FROM QUALITY) as 품질검사건수,
       (SELECT COUNT(*) FROM MATERIAL) as 재고건수,
       (SELECT COUNT(*) FROM BOARD) as 게시글수
FROM DUAL;
